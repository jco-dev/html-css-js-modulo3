// 1.1 Variables y Tipos de Datos

// var (antigua, evitar usarla)
var nombre = "Juan";

// let (para variables que cambian)
let edad = 25;
edad = 26;

// const (para valores constantes)
const PI = 3.14159;
// PI = 3.14; // Error

// Tipos de datos Principales
// Números
let numero = 42;
let decimal = 3.14;

// Strings (texto)
let texto = "Hola mundo";
let texto2 = 'También con comillas simples';
let template = `Puedo incluir variables: ${numero}`;

// Booleanos
let esVerdadero = true;
let esFalso = false;

// Undefined y Null
let sinDefinir; // undefined
let vacio = null;

// Arrays (listas)
let frutas = ["manzana", "pera", "uva"];

// Objetos
let persona = {
    nombre: "Ana",
    edad: 30
};